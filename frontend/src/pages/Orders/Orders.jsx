export const Orders = ({ setRoute }) => {
  const [order, setOrder] = useState(null);
  const [orders, setOrders] = useState(null);
  const [loading, setLoading] = useState(false);
  const printOrder = useRef();
  const handlePrint = useReactToPrint({
    content: () => printOrder.current,
  });

  const handleOrder = (id) => {
    setLoading(true);
    let order = orders.filter((item) => item.ID === id);
    setOrder(order[0]);
    setTimeout(() => {
      handlePrint();
      setLoading(false);
    }, 3000);
  };

  useEffect(() => {
    const getOrder = async () => {
      const orderList = await window.go.main.App.GetOrdersList(100, 0);
      setOrders(orderList);
    };
    getOrder();
  }, []);

  return (
    <Container>
      <Navbar/>
      <Stack>
        <Box sx={{ width: "100%", maxWidth: 360, bgcolor: "background.paper" }}>
          {orders &&
            orders.map((item) => {
              if (loading) return null;
              let orderText = `Orden de servicio: ${item.identifier}`;
              return (
                <nav
                  aria-label="main mailbox folders"
                  // key={item.ID}
                  onClick={() => handleOrder(item.ID)}
                >
                  <List>
                    <ListItem disablePadding>
                      <ListItemButton>
                        <ListItemIcon>
                          <InboxIcon />
                        </ListItemIcon>
                        <ListItemText primary={orderText} />
                      </ListItemButton>
                    </ListItem>
                  </List>
                </nav>
              );
            })}
        </Box>
      </Stack>
      {order && (
        <PrintOrder
          order={order}
          orderNumber={order.identifier}
          componentRef={printOrder}
          handlePrint={handlePrint}
        />
      )}
    </Container>
  );
};
